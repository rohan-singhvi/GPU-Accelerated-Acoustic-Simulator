#ifndef SIMULATION_H
#define SIMULATION_H

#include <vector>
#include <string>
#include "cuda_math.h"
#include "mesh_loader.h"

struct MaterialParams {
    float absorption;   // 0.0 to 1.0 (Energy loss)
    float scattering;   // 0.0 to 1.0 (Roughness/Diffusion)
    float transmission; // 0.0 to 1.0 (Sound passing through walls)
    float thickness;    // Wall thickness in meters
};

enum RoomType { SHOEBOX = 0, DOME = 1, MESH = 2 };

struct SimulationParams {
    int num_rays;
    RoomType room_type;
    float3 room_dims; // Box: L,W,H | Dome: R,0,0
    float3 source_pos;
    float3 listener_pos;
    std::string mesh_path;
    
    MaterialParams material;
};

// Main Entry Point (Dispatcher)
void run_simulation(const SimulationParams& params, const MeshData& mesh, std::vector<float>& ir);

// CPU Backend
void run_simulation_cpu(const SimulationParams& params, const MeshData& mesh, std::vector<float>& ir);

// GPU Backend
// We explicitly check if ENABLE_CUDA is defined.
#ifdef ENABLE_CUDA
    void run_simulation_gpu(const SimulationParams& params, const MeshData& mesh, std::vector<float>& ir);
#endif

#endif